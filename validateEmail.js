async function validateEmail(email) {
  const emailValue = email;
  const item = {
    source: "archivo",
    deleted: false,
    valid: true,
    email: emailValue,
  };
  const patternEmail = new RegExp(/^[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,}$/i);

  if (!patternEmail.test(emailValue)) {
    item.valid = false;
  }

  const sections = emailValue.split("@");

  if (sections.length !== 2) {
    item.valid = false;
  }

  const sectionName = sections[0];
  if (sectionName.length < 5) {
    item.valid = false;
  }

  const wordNotAllowed = [
    "no",
    "no_tiene",
    "nocorreo",
    "no_correo",
    "nocuenta",
    "no_cuenta",
    "noposee",
    "no_posee",
    "nohay",
    "no_hay",
    "notengo",
    "no_tengo",
  ];

  if (wordNotAllowed.includes(sectionName.toLowerCase())) {
    item.valid = false;
  }

  const domain = sections[1];
  if (!uniqueDomains.has(domain)) {
    uniqueDomains.add(domain);
    try {
      await new Promise((resolve, reject) => {
        dns.resolveMx(domain, (error) => {
          if (error) {
            item.valid = false;
            item.reason = `Dominio Invlido (${domain}): ${error.message}`;
            reject(error);
          } else {
            resolve();
          }
        });
      });
    } catch (error) {
      // Manejar el error si es necesario
    }
  } else {
    item.valid = false;
  }

  const countLetter = [...sectionName].filter((letter) => letter === sectionName[0]).length;
  if (countLetter === sectionName.length) {
    item.valid = false;
  }

  return item;
}

